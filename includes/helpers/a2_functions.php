		return strip_tags( $input , '<ul><ol><li><p><h3><h4><h5><strong><em><i><a>' );
	}
	
	/**
	 *  replace spaces with underscores for pretty urls
	 *
	 * @param $input
	 *
	 * @return null|string|string[]
	 */
	function urlString( $input )
	{
		
		return preg_replace( '/\s/' , '_' , strtolower( $input ) );
	}
	
	/**
	 * Trim string from start to limits end
	 *
	 * @param $trimMe
	 * @param $limit
	 *
	 * @return bool|string
	 */
	function trimString( $trimMe , $limit )
	{
		
		return substr( $trimMe , 0 , $limit );
		
	}
	
	/**
	 * Catches all exceptions
	 *
	 * @param $e
	 */
	function exceptionCatcher( $e )
	{
		
		
		if ( $e->getCode() === 1044 )
		{
			$returnMessage = 'Invalid database name';
		}
		elseif ( $e->getCode() === 1045 )
		{
			$returnMessage = 'Invalid database username or password';
		}
		elseif ( $e->getCode() === 1050 )
		{
			$returnMessage = 'Base table or view already exists';
		}
		elseif ( $e->getCode() === 1146 )
		{
			$returnMessage = 'Base table or view not found';
		}
		else
		{
			
			if ( sca_get_preference( 'showcase' , 'sca_errorlogging' ) )
			{
				
				$logging = new \Classes\Core\Logging();
				
				$logging->logs       = "<strong>Exception : </strong>" . $e->getMessage() . '<br><strong>Exception Code: </strong>' . $e->getCode();
				$logging->created_at = date( "Y-m-d H:i:s" );
				$logging->create();
			}
			
			$returnMessage = "<strong>Error Notice : </strong> something went wrong contact site admin";
		}
		
		echo $returnMessage;
	}
	
