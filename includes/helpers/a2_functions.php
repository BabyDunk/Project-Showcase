	/**
	 * Returns showcase ID from URL
	 *
	 * @return null|int
	 */
	function showcaseUriID()
	{
		
		$splitUri = explode( '/' , $_SERVER[ 'REQUEST_URI' ] );
		
		switch ( $splitUri[ 2 ] )
		{
			case 'showcase':
				
				return (int) $splitUri[ 3 ];
				break;
			
			default:
				
				return null;
				break;
		}
		
	}
	
	/**
	 * Bytes to KB to MB to GB conversion
	 *
	 * @param $bytes
	 *
	 * @return string
	 */
	function formatSizeUnits( $bytes )
	{
		
		if ( $bytes >= 1073741824 )
		{
			$bytes = number_format( $bytes / 1073741824 , 2 ) . ' GB';
		}
		elseif ( $bytes >= 1048576 )
		{
			$bytes = number_format( $bytes / 1048576 , 2 ) . ' MB';
		}
		elseif ( $bytes >= 1024 )
		{
			$bytes = number_format( $bytes / 1024 , 2 ) . ' KB';
		}
		elseif ( $bytes > 1 )
		{
			$bytes = $bytes . ' bytes';
		}
		elseif ( $bytes == 1 )
		{
			$bytes = $bytes . ' byte';
		}
		else
		{
			$bytes = '0 bytes';
		}
		
		return $bytes;
	}
	
