	/**
	 * Admin View
	 *
	 * @param       $path
	 * @param array $data
	 */
	function adminView( $path , array $data = [] )
	{
		
		// Laravel-Blade
		$views = SITE_ROOT . 'display/sc-panel/view';
		$cache = SITE_ROOT . 'display/sc-panel/cache';
		
		$restricted = [
			'statistics' ,
			'general_settings' ,
			'email_settings' ,
			'social_settings' ,
			'users' ,
			'promotions'
		];
		
		$blade = new Blade( $views , $cache );
		
		if ( ! isAdmin() )
		{
			if ( ! in_array( $path , $restricted , true ) )
			{
				echo $blade->view()->make( $path , $data )->render();
			}
		}
		else
		{
			echo $blade->view()->make( $path , $data )->render();
		}
	}
	
	/**
	 * Check if use has administrative permissions
	 *
	 * @return bool
	 */
	function isAdmin()
	{
		
		return ( isset( \Classes\Core\Session::instance()->user_id ) && ! empty( \Classes\Core\User::find_by_id( \Classes\Core\Session::instance()->user_id )->privilege ) ) ? true : false;
	}
	
	/**
	 * Return Root URL
	 *
	 * @param bool $encode // encode special characters
	 */
	function sca_base_url( $encode = true )
	{
		
		$url = ( isset( $_SERVER[ 'HTTPS' ] ) ? 'https' : 'http' ) . '://' . $_SERVER[ 'HTTP_HOST' ] . '/';
		
		if ( $encode )
		{
			htmlspecialchars( $url , ENT_QUOTES , 'UTF-8' );
		}
		
		echo strtolower( $url );
	}
	
	/**
	 * Return if empty else empty string
	 *
	 * @param $input
	 *
	 * @return string
	 */
	function notEmpty( $input )
	{
		
		return ! empty( $input ) ? $input : '';
	}
	
	/**
	 * Checks if value is empty but zero will return false
	 *
	 * @param $var
	 *
	 * @return bool
	 */
	function isEmpty( $var )
	{
		
		if ( is_numeric( $var ) )
		{
			return false;
		}
		elseif ( empty( $var ) )
		{
			return true;
		}
		else
		{
			return false;
		}
	}
	
	/**
	 * Strips all other tags other than which are allowed
